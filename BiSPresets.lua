WLLF.CURRENT_PHASE = 2 -- aktuelle Anniversary-Phase (Stand: Mai 2026)

-- Liefert das BiS-Preset für eine Spec in einer bestimmten Phase.
-- Fällt zurück auf die nächstgelegene vorhandene Phase, falls für die
-- angefragte Phase noch keine Daten vorliegen (z.B. weil eine Klasse bislang
-- nur mit Phase-5-Daten befüllt wurde). Gibt zusätzlich zurück, ob es sich
-- um einen Fallback handelt und welche Phase tatsächlich verwendet wurde.
function WLLF:GetBiSPreset(specKey, phase)
    local presets = WLLF.BiSPresets[specKey]
    if not presets then return nil end

    phase = phase or WLLF.CURRENT_PHASE

    if presets[phase] then
        return presets[phase], phase, false
    end

    -- Fallback: nächstniedrigere vorhandene Phase suchen, sonst nächsthöhere
    local bestLower, bestHigher
    for p in pairs(presets) do
        if p <= phase and (not bestLower or p > bestLower) then
            bestLower = p
        end
        if p >= phase and (not bestHigher or p < bestHigher) then
            bestHigher = p
        end
    end
    local fallbackPhase = bestLower or bestHigher
    if not fallbackPhase then return nil end
    return presets[fallbackPhase], fallbackPhase, true
end

-- Liefert eine sortierte Liste aller Phasen, für die eine Spec Daten besitzt.
function WLLF:GetAvailablePhases(specKey)
    local presets = WLLF.BiSPresets[specKey]
    if not presets then return {} end
    local list = {}
    for p in pairs(presets) do table.insert(list, p) end
    table.sort(list)
    return list
end

