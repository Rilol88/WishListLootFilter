local OTHER_SOURCE_KEYWORDS = {
    "Schmiedekunst", "Lederverarbeitung", "Schneiderei", "Juwelenschleifen",
    "Ingenieurskunst", "Badge of Justice", "Ehrenpunkte", "Exalted", "Revered",
    "Arena", "PvP", "Vendor", "Ruf",
